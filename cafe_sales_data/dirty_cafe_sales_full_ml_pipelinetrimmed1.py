"""**Reasoning**:
To perform K-fold cross-validation, I will import the necessary modules, initialize KFold, and then loop through each fold to train the model, make predictions, and calculate R-squared and Mean Squared Error. Finally, I will calculate and print the average and standard deviation of these metrics across all folds.


"""

from sklearn.model_selection import KFold
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
import numpy as np

# Initializing the    KFold
kf = KFold(n_splits=5, shuffle=True, random_state=42)

# Create empty lists to store R-squared and MSE scores for each fold
r2_scores = []
mse_scores = []

# Loop through each fold
for train_index, val_index in kf.split(X_selected):
    # Split data into training and validation sets for the current fold
    X_train_fold, X_val_fold = X_selected.iloc[train_index], X_selected.iloc[val_index]
    y_train_fold, y_val_fold = y.iloc[train_index], y.iloc[val_index]

    # Instantiate a LinearRegression model
    model_kf = LinearRegression()

    # Train the model
    model_kf.fit(X_train_fold, y_train_fold)

    # Make predictions on the validation set
    y_pred_fold = model_kf.predict(X_val_fold)

    # Calculate R-squared and Mean Squared Error for the current fold
    r2 = r2_score(y_val_fold, y_pred_fold)
    mse = mean_squared_error(y_val_fold, y_pred_fold)

    # Append scores to lists
    r2_scores.append(r2)
    mse_scores.append(mse)

# Calculate average and standard deviation of scores
average_r2 = np.mean(r2_scores)
std_r2 = np.std(r2_scores)
average_mse = np.mean(mse_scores)
std_mse = np.std(mse_scores)

# Print the results
print(f"Average R-squared across folds: {average_r2:.2f} (Std: {std_r2:.2f})")
print(f"Average Mean Squared Error across folds: {average_mse:.2f} (Std: {std_mse:.2f})")

